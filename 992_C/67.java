    static long mod=1000000007;
    static long mod_exp(long base,long exp)
    {
        long ans=1;
        base%=mod;
        while(exp>0)
        {
            if(exp%2==1)
            {
                ans*=base;
                ans%=mod;
            }
            exp/=2;
            base*=base;
            base%=mod;
        }
        return ans;
    }
    public static void main(String args[]) throws IOException
    {
        Scanner in=new Scanner(System.in);
        PrintWriter out=new PrintWriter(System.out);
        long x=in.nextLong();
        long k=in.nextLong();
        
        long ans=mod_exp(2,k+1);
        ans*=(x%mod);
        ans%=mod;
        ans-=mod_exp(2,k);
        ans%=mod;
        ans++;
        ans%=mod;
        if(ans<0)
            ans+=mod;
        if(x==0)
            ans=0;
        out.println(ans);
        out.close();
    }

