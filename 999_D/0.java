import java.util.ArrayList;
import java.util.Comparator;
import java.util.PriorityQueue;
import java.util.Scanner;
import java.util.TreeMap;

public class k{
	
	public static void main(String[] args) throws Exception 
	{ 
		Scanner s=new Scanner(System.in);

		PriorityQueue<pair> q=new PriorityQueue<>(new comp());
		
		int n=s.nextInt();
		int m=s.nextInt();
		
		long[] arr=new long[n];
		for(int i=0;i<n;i++) {
			arr[i]=s.nextLong();
		}
		
		ArrayList<Integer>[] brr=new ArrayList[m];
		
		for(int i=0;i<m;i++) {
			brr[i]=new ArrayList<>();
			
		}
		
		for(int i=0;i<n;i++) {
			brr[(int)arr[i]%m].add(i);
		}
		
		int k=n/m;
		int end=-1;
		for(int i=m-1;i>=0;i--) {
			if(brr[i].size()>k) {
				end=i;
				break;
			}
		}
		
		
		if(end==-1) {
			System.out.println(0);
			for(int i=0;i<n;i++) {
				System.out.print(arr[i]+" ");
			}
			System.out.println();
		}else {
			long ans=0;
			
			for(int i=0;i<m;i++) {
				if(brr[i].size()<k) {
					long req=k-brr[i].size();
					while(req>0) {
						long a=Integer.MAX_VALUE;
						long b=Integer.MAX_VALUE;
						
						if(!q.isEmpty()) {
							a=i-q.peek().rem;	
						}
						if(end>i) {
							b=(i+1)+(m-1-end);
						}
						
						if(a<=b) {
							ans=ans+a;
							pair ob=q.poll();
							arr[ob.in]=arr[ob.in]+a;
						}else {
							ans=ans+b;
							arr[brr[end].get(brr[end].size()-1)]=arr[brr[end].get(brr[end].size()-1)]+b;
							brr[end].remove(brr[end].size()-1);
							
							while(end>-1 && brr[end].size()<=k) {
								end--;
								if(end==-1) {
									break;
								}
							}
							
						}
						req--;
						
						
					}
					
					
					
				}else {
					for(int j=0;j<brr[i].size()-k;j++) {
						q.add(new pair(i,brr[i].get(j)));
					}
					
				}
				
			}
			
			
			System.out.println(ans);
			for(int i=0;i<n;i++) {
				System.out.print(arr[i]+" ");
			}
			System.out.println();
			
			
		}
		
		
	}
}
class pair{
	int rem;
	int in;
	public pair(int rem,int in) {
		this.rem=rem;
		this.in=in;
	}
}
class comp implements Comparator<pair>{
	public int compare(pair h,pair k) {
		return h.rem-k.rem;
	}
}
